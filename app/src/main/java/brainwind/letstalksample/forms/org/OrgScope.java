package brainwind.letstalksample.forms.org;

public class OrgScope
{

    String scope="";
    String region="";
    boolean hasOtherPartsInOtherAreas=false;

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public boolean isHasOtherPartsInOtherAreas() {
        return hasOtherPartsInOtherAreas;
    }

    public void setHasOtherPartsInOtherAreas(boolean hasOtherPartsInOtherAreas) {
        this.hasOtherPartsInOtherAreas = hasOtherPartsInOtherAreas;
    }


}
